

# MODULARIDAD

'''
- Es subdividir un sistema completo en subsistemas
- La modularidad es partir un sistema en muchso modulos con los cuales se pueden dar diferentes posicionamientos para recrear nuevos sistemas 
- Nos ayuda a mantener los moduos independientes, y en caso de un problema esto no nos joda todo el sistema si no que solo afecte dicho modulo

EJEMPLO:
- Una fichas de lego, cada una es independiente de la otra, pero unida nos sirven para contruir algo, además si una ficha se daña no necesariamente dañará toda nuestra estructura, también nos permite saber en donde esta el daño.

'''