
#  OPERADORES ARÍTMETICOS

# Nos permiten ejecutar calculos matematicos

# Suma
print(10 + 10)

# Resta
print(10 - 10)

#  Multiplicación
print(10 * 10)

#  Division
print(10 / 10)

#  Modulo o residuo de una división
print(10 % 10)

# Nos quedamos con el numero entero de una divisón, si da decimal solo agarrará el entero
print(10 // 10)

# Exponenciación
print(10 ** 10)

# Concatenación
print('hola' + 'puto')

# Multiplicar una cadena para que e muestre * 3 veces en pantalla
print('hola' * 3)
