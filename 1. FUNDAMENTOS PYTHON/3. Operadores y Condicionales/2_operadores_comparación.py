

#  Operadores de comparación

# RECUERDA que todos estos operadores de comparación nos arrojan resultados booleanos de verdadero o falso
# EJ
# 7 es mayor que 5 es verdadero o es falso NO hay otra respuesta y así con toos los demas

# Mayor que
print(7 > 5)

# Menor que
print(9 < 11)

# Menor o igual que
print(8 <= 20)

# Mayor o igual que
print(20 >= 5)

#  Igual a 
print(20 == 20)

#  Dieferente de
print(6 != 8)